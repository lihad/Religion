package Lihad.Religion.Abilities;

import Lihad.Religion.Religion;

public class Personal {

	/**
	 * This class runs player personal abilities that are subject to player rank
	 * 
	 * 
	 * 1 percentage chance to turn mob into chickens
     * 2 percentage chance to turn sheep into gold
     * 3 percentage chance zombie drops random potion
     * 4 percentage chance zombie pigman drops random enchant weapon
	 * 
	 */
	
	public static Religion plugin;


	public Personal(Religion instance) {
		plugin = instance;
	}
}
